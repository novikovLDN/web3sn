/**
 * Готовность страницы к анимации.
 *
 * ПРОБЛЕМА, КОТОРУЮ ЭТО РЕШАЕТ
 *
 * Экран загрузки — полноэкранный оверлей, но всё, что под ним, уже
 * смонтировано и живёт своей жизнью. Первый экран считал себя видимым
 * (он и правда в зоне видимости — просто сверху лежит шторка) и отыгрывал
 * появление имени, надзаголовка и кнопок за те полторы секунды, пока
 * пользователь смотрел на прелоадер.
 *
 * К моменту, когда шторка уходила, анимация была уже закончена. Человек
 * попадал на статичный текст — и самый дорогой момент сайта, первое
 * появление имени, пропадал впустую. Причём именно у тех, кто ждал дольше:
 * чем медленнее канал, тем надёжнее терялась анимация.
 *
 * РЕШЕНИЕ
 *
 * Общий флаг готовности. Пока он false, примитивы появления держат
 * начальное состояние независимо от того, что показывает IntersectionObserver.
 * Флаг поднимается, когда прелоадер закончил уходить, и только тогда
 * начинается движение.
 *
 * Значение по умолчанию — true. Это важно: любой экран, отрисованный вне
 * провайдера (тематические экраны услуг), должен анимироваться как обычно,
 * а не ждать сигнала, которого никто не пошлёт.
 */

import { createContext, useContext, type ReactNode } from 'react'

const IntroReadyContext = createContext(true)

export function IntroReadyProvider({
  ready,
  children,
}: {
  ready: boolean
  children: ReactNode
}) {
  return <IntroReadyContext.Provider value={ready}>{children}</IntroReadyContext.Provider>
}

/**
 * Можно ли начинать появление.
 *
 * Возвращает true вне провайдера — см. комментарий выше о значении
 * по умолчанию.
 */
export function useIntroReady() {
  return useContext(IntroReadyContext)
}
