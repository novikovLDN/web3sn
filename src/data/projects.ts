/** Анимированная иконка для карточки-заглушки (когда нет скриншота). */
export type IconName = 'spark' | 'cube' | 'orbit' | 'chip' | 'gyro'

export type ProjectCard = {
  /** Номер слева, например '01'. */
  number: string
  /** Метка-статус справа: 'В работе', 'Кейс', 'Готово'… */
  tag: string
  /** Заголовок. Перенос строки — символом \n. */
  title: string
  /** Короткое описание. */
  text: string
  /** Тема оформления: 'orange' | 'cream' | 'dark'. */
  theme: CardTheme
  /** Иконка-заглушка (если нет скриншота): 'spark'|'cube'|'orbit'|'chip'|'gyro'. */
  icon?: IconName
  /** Скриншот проекта. Файл кладём в public/projects/, здесь путь: '/projects/имя.jpg'. */
  image?: string
  /** Необязательная ссылка «Смотреть проект». */
  link?: string
}

