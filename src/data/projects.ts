/** Цветовая тема карточки. */
export type CardTheme = 'orange' | 'cream' | 'dark'

/**
 * Пресеты тем — только на токенах.
 * Хексы здесь запрещены: цвет живёт в одном месте (design/tokens.css),
 * иначе смена палитры превращается в поиск по всему проекту.
 */
export const CARD_THEMES: Record<CardTheme, { bg: string; fg: string; accent: string }> = {
  orange: { bg: 'var(--a)', fg: 'var(--n-50)', accent: 'var(--n-50)' },
  cream: { bg: 'var(--n-900)', fg: 'var(--n-50)', accent: 'var(--a)' },
  dark: { bg: 'var(--n-200)', fg: 'var(--n-900)', accent: 'var(--a)' },
}

/**
 * Анимированная иконка-заглушка.
 * Экспорт сохранён: его используют StaticIcon и MarqueeSection.
 */
export type IconName = 'spark' | 'cube' | 'orbit' | 'chip' | 'gyro'

/** Изображение кейса. alt обязателен — это и доступность, и SEO. */
export type ProjectImage = {
  /** Путь от корня сайта: '/projects/имя.jpg'. */
  src: string
  /** Что на изображении. Не «скриншот проекта», а по существу. */
  alt: string
}

/** Результат работы. Цифра отделена от формулировки — её место в вёрстке крупнее. */
export type ProjectOutcome = {
  /** Проверяемая цифра с единицей: '+38%', '2.1s', '×3'. Пусто — если цифры нет. */
  metric?: string
  /** Что именно изменилось. Одно предложение. */
  text: string
}

export type ProjectCard = {
  /** Настоящий кейс (true) или заготовка (false). Только true попадает на страницу. */
  filled: boolean

  // ── Кто и когда ────────────────────────────────────────────────
  /** Клиент или компания. Для внутренних работ — 'Личный проект'. */
  client: string
  /** Год сдачи: '2025'. Или период: '2024 — 2025'. */
  year: string
  /**
   * Роль в проекте. Ключевое поле для lead-позиционирования:
   * «Арт-директор», «Дизайн + разработка», «Ведущий дизайнер, команда 4».
   */
  role: string

  // ── Что за работа ──────────────────────────────────────────────
  /** Название кейса. Перенос строки — символом \n. */
  title: string
  /** Дисциплины-теги: ['Продуктовый дизайн', '3D', 'Фронтенд']. */
  disciplines: string[]

  // ── Суть кейса ─────────────────────────────────────────────────
  /** Задача: что было не так до начала работы. Без воды. */
  challenge: string
  /** Решение: что сделано и почему именно так. */
  solution: string
  /** Результат. Необязателен — отсутствие честнее выдумки. */
  outcome?: ProjectOutcome

  // ── Оформление и ссылки ────────────────────────────────────────
  /** Тема карточки: 'orange' | 'cream' | 'dark'. */
  theme: CardTheme
  /** Изображения. Первое — главное, оно и показывается крупно. */
  images: ProjectImage[]
  /** Ссылка на живой проект. Без неё кнопка просто не рендерится. */
  link?: string
  /** Подпись ссылки, если 'Смотреть проект' не подходит: 'Читать кейс'. */
  linkLabel?: string
  /** Иконка-заглушка, пока нет изображения. */
  icon?: IconName
}

