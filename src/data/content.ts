// ── Хроника ───────────────────────────────────────────────────────────
/**
 * Датированная хроника выпуска. Пришла на место двух удалённых секций:
 * счётчиков достижений и отзывов.
 *
 * ПОЧЕМУ ХРОНИКА, А НЕ ЦИФРЫ
 * ──────────────────────────
 * Счётчик («90+ проектов») — это утверждение о себе, которое посетитель
 * обязан принять на веру. Веры у человека, открывшего сайт минуту назад,
 * нет. Запись с датой работает иначе, и сразу по трём причинам:
 *
 *  • её нельзя подделать незаметно — дата и событие проверяемы;
 *  • она не протухает — счётчик надо обновлять руками, хроника растёт сама,
 *    а забытый счётчик хуже отсутствующего;
 *  • она сообщает непрерывность. Человек, который публикует записи годами,
 *    очевидно не ищет работу. Это и есть нужный сигнал недоступности —
 *    он сообщается фактом, а не заявлением.
 *
 * КАК ЭТО ЗАПОЛНЯТЬ
 * ─────────────────
 *  1. Только то, что было. Ни одной выдуманной записи: одна проверяемая
 *     ложь обесценивает весь список, включая правду.
 *  2. Событие, а не состояние. «Сдал редизайн личного кабинета банка» —
 *     событие. «Развиваюсь в 3D» — состояние, ему тут не место.
 *  3. NDA не мешает. Клиента можно не называть: «Сдал платёжный интерфейс
 *     финтех-продукта (под NDA)» — это полноценная запись.
 *  4. Регулярность важнее веса. Ровный ритм — примерно раз в 3–6 недель —
 *     читается сильнее, чем три громких записи и год тишины: список с
 *     дырой в году сообщает «здесь всё закончилось».
 *  5. Порядок — свежая сверху. Секция не сортирует, она выводит как есть.
 *  6. Восемь-двенадцать записей на главной достаточно. Длинный список
 *     превращается в архив и перестаёт быть ритмическим элементом.
 *  7. Ссылка необязательна и ставится только если ведёт на живую страницу.
 *     Мёртвая ссылка стоит дороже, чем её отсутствие.
 *
 * ⚠️ НИЖЕ — ЗАГЛУШКИ. Даты, события и ссылки НЕ ВЫДУМАНЫ намеренно:
 * все записи помечены filled: false и на живом сайте не показываются
 * (в режиме разработки видны, чтобы было что заполнять). Секция целиком
 * исчезает, пока нет ни одной записи с filled: true.
 */

