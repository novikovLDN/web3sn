/**
 * Стадии пайплайна — общий словарь экрана и стенда.
 *
 * Живёт отдельным модулем по одной причине: список стадий читают оба —
 * и DOM-часть (док, подписи, таблица метрик), и WebGL-часть (материалы,
 * свет). Если бы он лежал в экране, стенд тянул бы за собой весь экран;
 * если бы в стенде — экран тянул бы за собой three.js в основной бандл.
 *
 * Порядок здесь — не оформление, а реальная последовательность работы.
 * Именно поэтому нумерация 01–06 стоит на стадиях, а не на разделах:
 * у стадий номер несёт смысл (пятую нельзя сделать раньше третьей),
 * у разделов он был бы декорацией.
 */

export type PipelineStage = 'wireframe' | 'retopo' | 'uv' | 'clay' | 'ao' | 'beauty'

export type StageDef = {
  id: PipelineStage
  /** Порядковый номер в пайплайне. */
  n: string
  /** Короткое имя для дока — влезает в 390px. */
  short: string
  /** Полное имя стадии. */
  label: string
  /** Что физически показано на стенде. */
  what: string
  /** Почему эту стадию нельзя подделать финальной картинкой. */
  why: string
  /**
   * Температура стадии. Не украшение: во всех редакторах проходы
   * вьюпорта закодированы цветом, и человек из профессии читает
   * стадию по цвету раньше, чем по подписи. Одна температура на
   * стадию, никаких градиентов.
   */
  tint: string
  /** Метрики, которые на этой стадии выходят на первый план. */
  focus: MetricKey[]
}

export type MetricKey =
  | 'triangles'
  | 'vertices'
  | 'meshes'
  | 'materials'
  | 'textures'
  | 'texel'
  | 'calls'
  | 'bounds'

