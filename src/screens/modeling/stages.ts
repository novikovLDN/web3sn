/**
 * Метрики модели, посчитанные из самого файла.
 * Каждое поле — результат обхода геометрии или показание рендерера,
 * ни одно не задано вручную.
 */
export type ModelStats = {
  triangles: number
  vertices: number
  meshes: number
  materials: number
  textures: number
  /** Мегапиксели уникальных текстур — реальный вес в видеопамяти. */
  texturePixels: number
  /** Текселей на единицу мира. null — если считать не из чего. */
  texel: number | null
  /** Разрешение, от которого посчитана плотность (реальное или предполагаемое). */
  texelBasis: number
  /** Реальные ли текстуры у модели, или плотность посчитана «если натянуть». */
  texelAssumed: boolean
  /** Вызовов отрисовки за кадр — показание renderer.info, а не оценка. */
  calls: number
  /** Габариты в единицах сцены. */
  bounds: [number, number, number]
  /** Своя модель пользователя или демонстрационный объект. */
  source: 'user' | 'demo'
}
